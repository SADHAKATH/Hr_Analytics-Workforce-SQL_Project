-- DATE TIME FUNCTION
SELECT NOW();
SELECT CURDATE();
SELECT CURTIME();
SELECT DATE(NOW());
SELECT YEAR('2024-10-01'); -- 'YYYY-MM-DD'
SELECT MONTH('2024-10-01');
SELECT DAY('2024-10-01');
SELECT DAYNAME('2025-07-29'); -- DAY OF THE WEEK
SELECT DAYOFWEEK('2025-07-29');-- SUN-1
SELECT WEEK('2025-07-29'); -- WEEK OF THE YEAR IN NUMBER
SELECT DATE_ADD('2025-07-29', INTERVAL 5 DAY);
SELECT DATE_SUB('2025-07-29', INTERVAL 5 DAY);
SELECT DATEDIFF('2025-05-01', '2025-04-28');
SELECT STR_TO_DATE('2025-05-01','%Y-%m-%d');
/*
"yyyy-mm-dd" = "2025-06-19"
"yy-mm-dd"="25-06-19"
"yymmdd"="25Jun19th"
*/
select str_to_date('25-06-2025','%d-%m-%Y');
select str_to_date('2025-06-19','%Y-%m-%d');

select str_to_date('18-05-25','%d-%m-%y');
SELECT STR_TO_DATE('25May1st', '%y%M%D');
select '2025-07-29' as cur_date;
/*
%Y = 2025 - full year
%y=25 - short year
%m=10 - month in number
%M=Oct - month in words
%d=10 - date
%D = 10th- date with word
*/


